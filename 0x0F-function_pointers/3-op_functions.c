#include "3-calc.h"

/**
* op_add - main function
* Return: int
* @a: 1st number
* @b: 2nd number
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - main function
* Return: int
* @a: 1st number
* @b: 2nd number
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - main function
* Return: int
* @a: 1st number
* @b: 2nd number
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - main function
* Return: int
* @a: 1st number
* @b: 2nd number
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - main function
* Return: int
* @a: 1st number
* @b: 2nd number
*/

int op_mod(int a, int b)
{
	return (a % b);
}
